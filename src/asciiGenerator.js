/**
 * 生成 ASCII 艺术字
 * @param {string} text - 要转换的文本
 * @param {string} style - 字体样式: 'simple' | 'block'
 * @returns {string} ASCII 艺术字
 */
export function generateAscii(text, style = "block") {
  const font = style === "simple" ? FONT_3x5 : FONT_BLOCK;
  const height = style === "simple" ? 5 : 6;
  const upperText = text.toUpperCase();

  const lines = [];
  for (let row = 0; row < height; row++) {
    let line = "";
    for (const char of upperText) {
      const charArt = font[char] || font[" "];
      line += charArt[row] + " ";
    }
    lines.push(line);
  }

  return lines.join("\n");
}

/**
 * 将普通字符替换为 Unicode 方块字符
 * @param {string} ascii - ASCII 艺术字
 * @param {string} style - 方块样式: 'full' | 'light' | 'double'
 * @returns {string} 替换后的艺术字
 */
export function stylizeAscii(ascii, style = "full") {
  const styles = {
    full: { block: "█", half: "▓" },
    light: { block: "░", half: "▒" },
    double: { block: "╔", half: "═" },
  };

  const s = styles[style] || styles.full;
  return ascii.replace(/█/g, s.block);
}

/**
 * 添加边框
 * @param {string} ascii - ASCII 艺术字
 * @returns {string} 带边框的艺术字
 */
export function addBorder(ascii) {
  const lines = ascii.split("\n");
  const maxWidth = Math.max(...lines.map((l) => l.length));

  const top = "╔" + "═".repeat(maxWidth + 2) + "╗";
  const bottom = "╚" + "═".repeat(maxWidth + 2) + "╝";

  const bordered = lines.map((line) => {
    const padding = " ".repeat(maxWidth - line.length);
    return "║ " + line + padding + " ║";
  });

  return [top, ...bordered, bottom].join("\n");
}

export default { generateAscii, stylizeAscii, addBorder };
